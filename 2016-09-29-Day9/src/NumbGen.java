import java.util.*;


public class NumbGen {
	
	public static void main(String [] args){
		
		//You can choose the random seed number to start with
		
		Random scan = new Random();
		int x = scan.nextInt();
		System.out.println(x);
		
		int y = scan.nextInt();
		System.out.println(y);
		
		int z = scan.nextInt();
		System.out.println(z);
		
		
		
	}

}
